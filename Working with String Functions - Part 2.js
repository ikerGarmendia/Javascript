// Reemplazar "dog" con "cat" en la cadena
let stringOne = "The dog meows";
let replacedString = stringOne.replace("dog", "cat");

// Encontrar el índice de "over" en la cadena
let stringTwo = "The cow jumped over the moon";
let indexOfOver = stringTwo.indexOf("over");

// Encontrar el último índice de "never" en la cadena
let stringThree = "Never gonna give you up never gonna let you down";
let lastIndex = stringThree.lastIndexOf("never");

// Imprimir los resultados en la consola
console.log(replacedString);  // Salida: "The cat meows"
console.log(indexOfOver);     // Salida: 17
console.log(lastIndex);       // Salida: 31
