// Cadena proporcionada
let string = "Hello, what happened to all the pie?";

// Utilizar la función endsWith para verificar si la cadena termina con "pie?"
let endsWithResult = string.endsWith("pie?");

// Utilizar la función startsWith para verificar si la cadena comienza con "Hello,"
let startsWithResult = string.startsWith("Hello,");

// Imprimir los resultados en la consola
console.log(endsWithResult); // Devuelve true
console.log(startsWithResult); // Devuelve true
